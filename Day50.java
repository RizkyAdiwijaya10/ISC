public class Day50 {
    public static void main(String[] args) {
        //penjumlahan
        int a = 1;
        a += 10;
        System.out.println("nilai a = " + a);
        
        //pengurangan
        int b = 10;
        b -= 3;
        System.out.println("nilai b = " + b);
        
        //perkalian
        int c = 5;
        c *= 5;
        System.out.println("nilai c = " + c);
        
        //pembagian
        int d = 20;
        d /= 2;
        System.out.println("nilai d = " + d);
        
        //modulus
        int e = 20;
        e %= 6;
        System.out.println("nilai e = " + e);
    }
}