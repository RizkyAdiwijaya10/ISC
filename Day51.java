package informatic_study_club;


public class Day51 {
    public static void main(String[] args) {
        // unary + dan -
        int a = 5;
        System.out.println("nilai a = " + +a);
        System.out.println("nilai a = " + -a);
        
        // unary increment dan decrement
        int angka = 10;
        angka++;
        System.out.println("nilai angka = " + angka);
        int angka2 = 10;
        angka2--;
        System.out.println("nilai angka = " + angka2);
        
        // unary negasi (!)
        
        boolean data = true;
        System.out.println("nilai boolean = " + data);
        System.out.println("nilai boolean = " + !data);
    }
 
}
