public class Day79 {
    public static void main(String[] args) {
        // Nilai satuan dalam tipe data integer
        int nilaiSatuan = 5;

        // Konversi ke tipe data desimal (float)
        float nilaiDesimalFloat = (float) nilaiSatuan;

        // Konversi ke tipe data desimal (double)
        double nilaiDesimalDouble = (double) nilaiSatuan;

        // Menampilkan hasil konversi
        System.out.println("Nilai Satuan (Integer): " + nilaiSatuan);
        System.out.println("Nilai Desimal (Float): " + nilaiDesimalFloat);
        System.out.println("Nilai Desimal (Double): " + nilaiDesimalDouble);
    }
}
